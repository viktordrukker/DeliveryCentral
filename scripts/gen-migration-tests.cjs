#!/usr/bin/env node

/**
 * DM-R-13 — per-migration contract generator.
 *
 * Writes a single `test/integration/migration-contracts.generated.spec.ts`
 * file containing one `describe()` block per migration directory under
 * prisma/migrations/. Each describe asserts:
 *
 *   1. migration.sql exists and is non-empty.
 *   2. Classification marker (REVERSIBLE.md or FORWARD_ONLY.md) matches
 *      the frozen posture.
 *   3. SHA-256 of migration.sql matches a digest frozen at generation
 *      time. Any later edit requires regenerating — this is the
 *      "ossification" property the DM-R plan calls for.
 *   4. If REVERSIBLE: rollback.sql exists + non-empty + frozen SHA-256.
 *   5. If FORWARD_ONLY: rollback.sql must NOT exist (fake-rollback guard).
 *
 * Why a single file rather than one file per migration?
 * Per-file specs OOM Jest workers (48 files × ts-jest compilation
 * overhead > container memory). The per-migration describe-block is
 * what matters for human readability + diff review.
 *
 * Workflow:
 *   npm run test:migrations:gen     → regenerate from disk
 *   npm run test:migrations:check   → verify in-place (CI gate)
 *
 * Complementary:
 *   DM-R-4  classification marker lint (live files)
 *   DM-R-9  category-level assertions across migrations
 *   DM-R-11 bit-accurate rollback round-trip (actual DB)
 *   DM-R-13 per-migration frozen contract (this)
 */

const fs = require('node:fs');
const path = require('node:path');
const crypto = require('node:crypto');

const rootDir = path.resolve(__dirname, '..');
const migrationsDir = path.join(rootDir, 'prisma', 'migrations');
const outFile = path.join(rootDir, 'test', 'integration', 'migration-contracts.generated.spec.ts');

function sha256(buf) {
  return crypto.createHash('sha256').update(buf).digest('hex');
}

function readIfExists(p) {
  return fs.existsSync(p) ? fs.readFileSync(p) : null;
}

function describeFor(dirName) {
  const abs = path.join(migrationsDir, dirName);
  const migrationSqlBuf = readIfExists(path.join(abs, 'migration.sql'));
  const rollbackSqlBuf = readIfExists(path.join(abs, 'rollback.sql'));
  const hasReversible = fs.existsSync(path.join(abs, 'REVERSIBLE.md'));
  const hasForwardOnly = fs.existsSync(path.join(abs, 'FORWARD_ONLY.md'));

  const migrationSha = migrationSqlBuf ? sha256(migrationSqlBuf) : '';
  const rollbackSha = rollbackSqlBuf ? sha256(rollbackSqlBuf) : '';

  const posture = hasReversible ? 'REVERSIBLE' : hasForwardOnly ? 'FORWARD_ONLY' : 'UNCLASSIFIED';

  const body = [
    `describe('${dirName}', () => {`,
    `  const migrationDir = path.join(migrationsRoot, ${JSON.stringify(dirName)});`,
    ``,
    `  it('migration.sql exists + non-empty', () => {`,
    `    const p = path.join(migrationDir, 'migration.sql');`,
    `    expect(fs.existsSync(p)).toBe(true);`,
    `    expect(fs.statSync(p).size).toBeGreaterThan(0);`,
    `  });`,
    ``,
    `  it('posture matches frozen classification (${posture})', () => {`,
    `    const hasReversible = fs.existsSync(path.join(migrationDir, 'REVERSIBLE.md'));`,
    `    const hasForwardOnly = fs.existsSync(path.join(migrationDir, 'FORWARD_ONLY.md'));`,
    `    const p = hasReversible ? 'REVERSIBLE' : hasForwardOnly ? 'FORWARD_ONLY' : 'UNCLASSIFIED';`,
    `    expect(p).toBe(${JSON.stringify(posture)});`,
    `  });`,
    ``,
    `  it('migration.sql SHA-256 is frozen', () => {`,
    `    expect(sha256File(path.join(migrationDir, 'migration.sql'))).toBe(${JSON.stringify(migrationSha)});`,
    `  });`,
  ];

  if (hasReversible) {
    body.push(
      ``,
      `  it('rollback.sql exists + non-empty (REVERSIBLE)', () => {`,
      `    const p = path.join(migrationDir, 'rollback.sql');`,
      `    expect(fs.existsSync(p)).toBe(true);`,
      `    expect(fs.statSync(p).size).toBeGreaterThan(0);`,
      `  });`,
      ``,
      `  it('rollback.sql SHA-256 is frozen', () => {`,
      `    expect(sha256File(path.join(migrationDir, 'rollback.sql'))).toBe(${JSON.stringify(rollbackSha)});`,
      `  });`,
    );
  } else if (hasForwardOnly) {
    body.push(
      ``,
      `  it('FORWARD_ONLY must not carry rollback.sql (fake rollbacks silently lose data)', () => {`,
      `    expect(fs.existsSync(path.join(migrationDir, 'rollback.sql'))).toBe(false);`,
      `  });`,
    );
  }

  body.push(`});`);
  return body.join('\n');
}

function buildSpec(dirs) {
  const header = [
    `// AUTO-GENERATED by scripts/gen-migration-tests.cjs — DO NOT EDIT BY HAND.`,
    `// Regenerate via \`npm run test:migrations:gen\` after intentional migration changes.`,
    ``,
    `import crypto from 'node:crypto';`,
    `import fs from 'node:fs';`,
    `import path from 'node:path';`,
    ``,
    `const migrationsRoot = path.resolve(__dirname, '../../prisma/migrations');`,
    ``,
    `function sha256File(p: string): string {`,
    `  return crypto.createHash('sha256').update(fs.readFileSync(p)).digest('hex');`,
    `}`,
    ``,
    `describe('DM-R-13 per-migration contract', () => {`,
  ];

  const blocks = dirs.map((d) => describeFor(d).replace(/^/gm, '  '));
  const footer = [`});`, ``];

  return `${header.join('\n')}\n${blocks.join('\n\n')}\n${footer.join('\n')}`;
}

function main() {
  const mode = process.argv[2]; // 'check' | undefined

  if (!fs.existsSync(migrationsDir)) {
    console.error(`\x1b[31m✗\x1b[0m migrations dir not found: ${migrationsDir}`);
    process.exit(1);
  }
  fs.mkdirSync(path.dirname(outFile), { recursive: true });

  // Clean up legacy per-file specs from an earlier layout.
  const legacyDir = path.join(rootDir, 'test', 'integration', 'migrations');
  if (fs.existsSync(legacyDir)) {
    for (const file of fs.readdirSync(legacyDir)) {
      if (file.endsWith('.spec.ts')) fs.unlinkSync(path.join(legacyDir, file));
    }
    // Remove the dir if empty.
    if (fs.readdirSync(legacyDir).length === 0) fs.rmdirSync(legacyDir);
  }

  const dirs = fs
    .readdirSync(migrationsDir, { withFileTypes: true })
    .filter((e) => e.isDirectory())
    .map((e) => e.name)
    .sort();

  const content = buildSpec(dirs);

  if (mode === 'check') {
    if (!fs.existsSync(outFile) || fs.readFileSync(outFile, 'utf8') !== content) {
      console.error(`\x1b[31m✗\x1b[0m DM-R-13: generated spec out of sync with migrations.`);
      console.error(`  Run: npm run test:migrations:gen`);
      process.exit(1);
    }
    console.log(`\x1b[32m✓\x1b[0m DM-R-13: contract spec in sync with ${dirs.length} migrations.`);
  } else {
    fs.writeFileSync(outFile, content);
    console.log(`\x1b[32m✓\x1b[0m DM-R-13: wrote ${dirs.length} contract describe blocks to ${path.relative(rootDir, outFile)}`);
  }
}

main();
